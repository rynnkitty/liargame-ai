// ──────────────────────────────────────────────
// 템플릿 치환 유틸
// ──────────────────────────────────────────────
export function fillTemplate(
  template: string,
  vars: Record<string, string>
): string {
  return Object.entries(vars).reduce(
    (result, [key, value]) => result.replace(new RegExp(`\\{\\{${key}\\}\\}`, 'g'), value),
    template
  );
}
