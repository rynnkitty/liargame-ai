/**
 * 특정 카테고리에서 키워드를 랜덤으로 선택합니다.
 */
export function pickRandomKeyword(categoryId: string): string {
  const list = KEYWORDS[categoryId];
  if (!list || list.length === 0) throw new Error(`Unknown category: ${categoryId}`);
  return list[Math.floor(Math.random() * list.length)];
}
